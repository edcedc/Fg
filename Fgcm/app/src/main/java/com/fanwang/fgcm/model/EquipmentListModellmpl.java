package com.fanwang.fgcm.model;

import com.blankj.utilcode.util.ToastUtils;
import com.fanwang.fgcm.bean.BaseListBean;
import com.fanwang.fgcm.bean.BaseResponseBean;
import com.fanwang.fgcm.bean.DataBean;
import com.fanwang.fgcm.callback.Code;
import com.fanwang.fgcm.controller.CloudApi;
import com.fanwang.fgcm.presenter.listener.OnEquipmentListListener;
import com.lzy.okgo.model.Response;

import java.util.List;

import io.reactivex.Observer;
import io.reactivex.android.schedulers.AndroidSchedulers;
import io.reactivex.disposables.Disposable;
import io.reactivex.functions.Consumer;

/**
 * Created by edison on 2018/5/3.
 */

public class EquipmentListModellmpl implements EquipmentListModel {

    @Override
    public void ajaxLabelListModelLabelId(final OnEquipmentListListener listener) {
        CloudApi.labelListModelLabelId()
                .doOnSubscribe(new Consumer<Disposable>() {
                    @Override
                    public void accept(Disposable disposable) throws Exception {
                        listener.showLoading();
                    }
                })
                .observeOn(AndroidSchedulers.mainThread())
                .subscribe(new Observer<Response<BaseResponseBean<List<DataBean>>>>() {
                    @Override
                    public void onSubscribe(Disposable d) {
                        listener.onAddDisposable(d);
                    }

                    @Override
                    public void onNext(Response<BaseResponseBean<List<DataBean>>> baseResponseBeanResponse) {
                        if (baseResponseBeanResponse.body().code == Code.CODE_SUCCESS){
                            List<DataBean> data = baseResponseBeanResponse.body().data;
                            if (data != null && data.size() != 0){
                                listener.onModelLabelIdSuccess(data);
                            }else {
                                ToastUtils.showShort("当前位置没设备");
                            }
                        }else {
                            ToastUtils.showShort(baseResponseBeanResponse.body().desc);
                        }
                    }

                    @Override
                    public void onError(Throwable e) {
                        listener.onError(e);
                    }

                    @Override
                    public void onComplete() {
                        listener.hideLoading();
                    }
                });
    }

    @Override
    public void ajaxEqpPageTerminalEquipment(int range, String modelId, double latitude, double longitude, int pagerNumber, final OnEquipmentListListener listener) {
        CloudApi.eqpSearch(modelId,range,latitude,longitude,pagerNumber)
                .doOnSubscribe(new Consumer<Disposable>() {
                    @Override
                    public void accept(Disposable disposable) throws Exception {

                    }
                })
                .observeOn(AndroidSchedulers.mainThread())
                .subscribe(new Observer<Response<BaseResponseBean<BaseListBean<DataBean>>>>() {
                    @Override
                    public void onSubscribe(Disposable d) {
                        listener.onAddDisposable(d);
                    }

                    @Override
                    public void onNext(Response<BaseResponseBean<BaseListBean<DataBean>>> beanResponse) {
                        if (beanResponse.body().code == Code.CODE_SUCCESS){
                            BaseListBean<DataBean> data = beanResponse.body().data;
                            if (data != null){
                                List<DataBean> list = data.getList();
                                if (list != null && list.size() != 0){
                                    listener.onSuccess(list);
                                    listener.onRefreshLayoutMode(data.getTotalRow());
                                }else {
                                    ToastUtils.showShort("当前位置没设备");
                                }
                            }
                        }
                    }

                    @Override
                    public void onError(Throwable e) {
                        listener.onError(e);
                    }

                    @Override
                    public void onComplete() {
                        listener.hideLoading();
                    }
                });
    }

    @Override
    public void onPageTerminalEquipment(String regionId, String provinceArray, String cityArray, String areaArray, int pagerNumber, final OnEquipmentListListener listener) {
        CloudApi.eqpPageTerminalEquipment(regionId,provinceArray,cityArray,areaArray,pagerNumber)
                .doOnSubscribe(new Consumer<Disposable>() {
                    @Override
                    public void accept(Disposable disposable) throws Exception {

                    }
                })
                .observeOn(AndroidSchedulers.mainThread())
                .subscribe(new Observer<Response<BaseResponseBean<BaseListBean<DataBean>>>>() {
                    @Override
                    public void onSubscribe(Disposable d) {
                        listener.onAddDisposable(d);
                    }

                    @Override
                    public void onNext(Response<BaseResponseBean<BaseListBean<DataBean>>> beanResponse) {
                        if (beanResponse.body().code == Code.CODE_SUCCESS){
                            BaseListBean<DataBean> data = beanResponse.body().data;
                            if (data != null){
                                List<DataBean> list = data.getList();
                                if (list != null && list.size() != 0){
                                    listener.onSuccess(list);
                                    listener.onRefreshLayoutMode(data.getTotalRow());
                                }else {
                                    ToastUtils.showShort("当前位置没设备");
                                }
                            }
                        }
                    }

                    @Override
                    public void onError(Throwable e) {
                        listener.onError(e);
                    }

                    @Override
                    public void onComplete() {
                        listener.hideLoading();
                    }
                });
    }

}
